# One-off script: builds Russian user guide for adding activities via UI.
from pathlib import Path

from docx import Document
from docx.shared import Pt
from docx.enum.text import WD_PARAGRAPH_ALIGNMENT

OUT = Path(__file__).resolve().parent / "TimeTracker_guide_adding_activities_ru.docx"


def add_heading(doc, text, level=1):
    p = doc.add_heading(text, level=level)
    return p


def add_para(doc, text, bold=False):
    p = doc.add_paragraph()
    run = p.add_run(text)
    run.bold = bold
    run.font.size = Pt(11)
    return p


def add_bullets(doc, items):
    for item in items:
        p = doc.add_paragraph(item, style="List Bullet")
        for run in p.runs:
            run.font.size = Pt(11)


def main():
    doc = Document()
    title = doc.add_heading(
        "TimeTracker: добавление операций (активностей) через веб-интерфейс",
        0,
    )
    title.alignment = WD_PARAGRAPH_ALIGNMENT.CENTER

    add_para(
        doc,
        "В приложении учёт ведётся по активностям (операциям/участкам). Каждая активность "
        "привязана к зоне склада, имеет штрихкод (ШК), полное и короткое наименование; "
        "поле «Метрика» необязательно и используется для справки.",
    )

    add_heading(doc, "1. Как открыть раздел управления", level=1)
    add_bullets(
        doc,
        [
            "Откройте главную страницу TimeTracker в браузере.",
            "В правой части шапки нажмите кнопку с иконкой шестерёнки (настройки). Подсказка: «Админ: Активности».",
            "Откроется окно «Управление активностями».",
        ],
    )

    add_heading(doc, "2. Массовый импорт из Excel", level=1)
    add_para(
        doc,
        "Удобно, когда нужно добавить много строк из таблицы.",
    )
    add_bullets(
        doc,
        [
            "В блоке «Массовый импорт из Excel» выберите зону: Терминал 3, Терминал 2, Мезонин или Балк.",
            "В Excel подготовьте три колонки в порядке: Наименование, ШК, Метрика.",
            "Выделите нужные ячейки (включая строки данных), скопируйте (Ctrl+C).",
            "Вставьте текст в большое поле ввода в окне импорта (Ctrl+V). Колонки должны разделяться табуляцией — так Excel копирует по умолчанию.",
            "Нажмите «Импортировать».",
            "Система покажет результат: сколько записей добавлено и сколько пропущено (например, из-за дубликата ШК в базе).",
        ],
    )
    add_para(
        doc,
        "Автоматическое короткое имя: из полного наименования берутся последние три сегмента, "
        "разделённые символом «_» (подчёркивание), не длиннее 99 символов. При необходимости "
        "короткое имя можно задать вручную через добавление одной записи (см. ниже).",
        bold=False,
    )

    add_heading(doc, "3. Добавление одной активности вручную", level=1)
    add_bullets(
        doc,
        [
            "Раскройте блок «+ Добавить одну активность вручную».",
            "Заполните поля: ШК — полное название — короткое название. Поле «Метрика» можно оставить пустым.",
            "Нажмите «Добавить». Список в таблице ниже обновится.",
        ],
    )
    add_para(
        doc,
        "Важно: в текущей версии интерфейса для одиночного добавления зона в форме не выбирается — "
        "запись создаётся для зоны по умолчанию (Терминал 3). Для других зон используйте массовый импорт "
        "с выбором нужной зоны.",
    )

    add_heading(doc, "4. Просмотр и удаление", level=1)
    add_bullets(
        doc,
        [
            "В нижней части окна отображается таблица всех активностей: зона, ШК, полное и короткое имя, метрика.",
            "Чтобы удалить запись, нажмите иконку корзины в строке и подтвердите удаление.",
        ],
    )

    add_heading(doc, "5. После добавления: использование на терминале", level=1)
    add_bullets(
        doc,
        [
            "На главном экране выберите ту же зону, для которой создана активность.",
            "Отсканируйте ШК активности, затем ШК сотрудника — смена начнётся, если данные в базе согласованы с зоной.",
        ],
    )

    add_heading(doc, "6. Типичные проблемы", level=1)
    add_bullets(
        doc,
        [
            "Сообщение об отсутствии активности при сканировании — проверьте, что ШК введён без лишних пробелов и что активность создана для выбранной зоны.",
            "Импорт «пропускает» строки — часто из-за уже существующего ШК; проверьте таблицу и отчёт после импорта.",
        ],
    )

    doc.save(OUT)
    print(f"Written: {OUT}")


if __name__ == "__main__":
    main()

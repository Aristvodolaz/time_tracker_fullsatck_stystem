-- Таблица подразделений: обязательно храним номер подразделения + наименование
-- Маппинг: employee.manning_id -> Departments.departmentNumber -> название в отчёте "Основной Код активности (Подразделение)"

-- Миграция: если таблица уже была с колонкой [id], переименовать в [departmentNumber]
IF EXISTS (SELECT * FROM sys.objects WHERE object_id = OBJECT_ID(N'[dbo].[Departments]') AND type in (N'U'))
   AND COL_LENGTH('dbo.Departments', 'id') IS NOT NULL
   AND COL_LENGTH('dbo.Departments', 'departmentNumber') IS NULL
BEGIN
    EXEC sp_rename 'dbo.Departments.id', 'departmentNumber', 'COLUMN';
END
GO

IF NOT EXISTS (SELECT * FROM sys.objects WHERE object_id = OBJECT_ID(N'[dbo].[Departments]') AND type in (N'U'))
BEGIN
    CREATE TABLE [dbo].[Departments] (
        [departmentNumber] INT NOT NULL PRIMARY KEY,  -- номер подразделения (обязательное хранение)
        [name] NVARCHAR(500) NOT NULL,               -- наименование подразделения
        [createdAt] DATETIME DEFAULT GETDATE()
    );
    CREATE INDEX IX_Departments_Number ON [dbo].[Departments] ([departmentNumber]);
END
GO


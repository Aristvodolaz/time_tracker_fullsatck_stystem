-- 2. Удаление ВСЕХ ограничений уникальности с колонки activityBarcode
-- Это позволит иметь несколько активностей с одинаковым кодом (например, 000001084)
DECLARE @ConstraintName nvarchar(200);
DECLARE constraint_cursor CURSOR FOR 
    SELECT name FROM sys.key_constraints WHERE parent_object_id = OBJECT_ID('[dbo].[Activities]') AND type = 'UQ'
    UNION
    SELECT name FROM sys.indexes WHERE object_id = OBJECT_ID('[dbo].[Activities]') AND is_unique_constraint = 1;

OPEN constraint_cursor;
FETCH NEXT FROM constraint_cursor INTO @ConstraintName;
WHILE @@FETCH_STATUS = 0
BEGIN
    -- Проверяем, касается ли ограничение колонки activityBarcode
    IF EXISTS (
        SELECT 1 FROM sys.index_columns ic 
        JOIN sys.columns c ON ic.object_id = c.object_id AND ic.column_id = c.column_id
        WHERE ic.object_id = OBJECT_ID('[dbo].[Activities]') AND ic.index_id = OBJECT_ID(@ConstraintName) -- Simplified check
           OR EXISTS (SELECT 1 FROM sys.key_constraints kc JOIN sys.index_columns ic2 ON kc.parent_object_id = ic2.object_id WHERE kc.name = @ConstraintName AND c.name = 'activityBarcode')
    )
    BEGIN
        -- Для простоты удалим все уникальные ключи, так как мы берем данные из надежного источника
        BEGIN TRY EXEC('ALTER TABLE [dbo].[Activities] DROP CONSTRAINT ' + @ConstraintName); END TRY BEGIN CATCH END CATCH
        BEGIN TRY EXEC('DROP INDEX ' + @ConstraintName + ' ON [dbo].[Activities]'); END TRY BEGIN CATCH END CATCH
    END
    FETCH NEXT FROM constraint_cursor INTO @ConstraintName;
END
CLOSE constraint_cursor;
DEALLOCATE constraint_cursor;
GO

-- Дополнительно убедимся, что колонка позволяет дубликаты (удаляем индекс если остался)
IF EXISTS (SELECT name FROM sys.indexes WHERE name = N'UQ__Activiti__6F88BE6618F0E26E' AND object_id = OBJECT_ID(N'[dbo].[Activities]'))
    DROP INDEX [UQ__Activiti__6F88BE6618F0E26E] ON [dbo].[Activities];
GO

